    private void hapusPemilikButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusPemilikButtonActionPerformed
        clearTextPemilik();
        setEditDeleteButtonPemilik(false);
        setComponentsPemilik(false);
        
        int opsi = JOptionPane.showConfirmDialog(rootPane, "Yakin Ingin Hapus ?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        if( opsi == JOptionPane.NO_OPTION || opsi == JOptionPane.CLOSED_OPTION){
            idPemilik = -1;
            return;
        }
        
        pc.deleteDataPemilik(idPemilik);
        idPemilik = -1;
        showPemilik();
    }//GEN-LAST:event_hapusPemilikButtonActionPerformed

