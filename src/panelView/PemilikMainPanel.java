    private void doSearchPemilik(){
        if(searchPemilikInputTextField.getText().isEmpty())
            return;
            Pemilik p = pc.searchPemilikById(Integer.parseInt(searchPemilikInputTextField.getText()));
            if(p == null ){
                JOptionPane.showMessageDialog(rootPane, "NOT FOUND !!!");
                return;
            }
            setEditDeleteButtonPemilik(true);
            clearTextPemilik();
            
            namaPemilikInputTextField.setText(p.getNama());
            nomorTeleponInputTextField.setText(p.getNotelp());
            umurInputTextField.setText(String.valueOf(p.getUmur()));
            
            idPemilik = p.getId_pemilik();
    }
    
