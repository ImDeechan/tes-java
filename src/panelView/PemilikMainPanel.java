    private void simpanCustomerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanCustomerButtonActionPerformed
        // TODO add your handling code here:
        if(actionPemilik == null)
            return;
        try{
            inputKosongCustomerException();
            noTelponException();
            if(JOptionPane.showConfirmDialog(rootPane, "yakin ingin melakukan " + actionPemilik + "?") == JOptionPane.CLOSED_OPTION)
                return;
            switch(actionPemilik){
            case "add":
                p = new Pemilik(namaPemilikInputTextField.getText(), Integer.parseInt(umurInputTextField.getText()), nomorTeleponInputTextField.getText());
                pc.insertDataPemilik(p);
                clearTextPemilik();
                setEditDeleteButtonPemilik(false);
                setComponentsPemilik(false);
                break;
            case "update":
                p = new Pemilik(namaPemilikInputTextField.getText(), Integer.parseInt(umurInputTextField.getText()), nomorTeleponInputTextField.getText());
                pc.updateDataPemilik(p, idPemilik);
                clearTextPemilik();
                setEditDeleteButtonPemilik(false);
                setComponentsPemilik(false);
                break;
            default:
                break;
        }
        showPemilik();
        actionPemilik = null;
        }catch(InputKosongException e){
            System.out.println(e);
        }catch(NoTelponException e){
            JOptionPane.showMessageDialog(rootPane, e.message());
        };
    }//GEN-LAST:event_simpanCustomerButtonActionPerformed


