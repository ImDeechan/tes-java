    private void simpanKendaraanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanKendaraanButtonActionPerformed
        if(actionAnabul == null)
            return;
        try{
            inputKosongAnabulException();
            InputSpecialAtributeAnabulException();
            
            if(JOptionPane.showConfirmDialog(rootPane, "yakin ingin melakukan " + actionAnabul + "?") == JOptionPane.CLOSED_OPTION)
                return;

            switch (actionAnabul){
                case "add":
                    if(anjingIsSelected()){
                        an = new Anjing(specialAtributeInputTextfield.getText(), namaAnabulInputTextField.getText(), 
                                jenisAnabulInputButton.getText(), Integer.parseInt(umurAnabulInputTextfield.getText()));
                        anc.insertDataAnabul(an);
                    }else{
                        kcg = new Kucing(specialAtributeInputTextfield.getText(), namaAnabulInputTextField.getText(), 
                                jenisAnabulInputButton.getText(), Integer.parseInt(umurAnabulInputTextfield.getText()));
                        kc.insertDataAnabul(kcg);
                    }
                    clearText();
                    setAnabulEditDeleteButton(false);
                    setComponentsAnabul(false);
                    showAnabul();
                    break;
                case "update":
                    if(anjingIsSelected()){
                        an = new Anjing(specialAtributeInputTextfield.getText(),idAnabulInputTextField.getText(), namaAnabulInputTextField.getText(), 
                                jenisAnabulInputButton.getText(), Integer.parseInt(umurAnabulInputTextfield.getText()));
                        anc.updateDataAnabul(an);
                    }else{
                        kcg = new Kucing(specialAtributeInputTextfield.getText(),idAnabulInputTextField.getText(), namaAnabulInputTextField.getText(), 
                                jenisAnabulInputButton.getText(), Integer.parseInt(umurAnabulInputTextfield.getText()));
                        kc.updateDataAnabul(kcg);
                    }
                    clearText();
                    setAnabulEditDeleteButton(false);
                    setComponentsAnabul(false);
                    showAnabul();
                    break;
                default:
                    break;
            }
            actionAnabul = null;
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(rootPane, e.message());
        }catch(InputSpecialAtributeException e){
            JOptionPane.showMessageDialog(rootPane, e.message(jenisAnabulInputButton.getText()));
        }
    }//GEN-LAST:event_simpanKendaraanButtonActionPerformed

