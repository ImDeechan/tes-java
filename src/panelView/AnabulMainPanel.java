    private boolean anjingIsSelected(){
        return jenisAnabulInputButton.getText().equals("Anjing");
    }
    
    private void doSearchAnabul(){
        if(searchAnabulInputTextField.getText().isEmpty())
            return;
        
            a = kc.searchDataAnabul(searchAnabulInputTextField.getText());
            
        if(a != null){
            setAnabulEditDeleteButton(true);
            idAnabulInputTextField.setText(a.getId_anabul());
            namaAnabulInputTextField.setText(a.getNama());
            umurAnabulInputTextfield.setText(Integer.toString(a.getUmur()));
            jenisAnabulInputButton.setText(a.getJenis());
            setSpecialAtributeLabel();
            specialAtributeInputTextfield.setText(a.getSpecial());
        }else{
            JOptionPane.showMessageDialog(rootPane, "NOT FOUND !!!");
        }
    }

    private void setSpecialAtributeLabel(){
        if(jenisAnabulInputButton.getText().equals("Anjing")){
            specialAtributeInputLabel.setText("Ras");
        }else{
            specialAtributeInputLabel.setText("Jenis Bulu");
        }
    }
    
